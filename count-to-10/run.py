# For loop
for i in range(10): # range starts from 0
    i += 1 # or i = i + 1
    print('[FOR] Current iteration is', i)

# For range 
for i in range(1, 11): # increase by 2 - range(1, 11, 2)
    print('[FOR RANGE] Current iteration is', i)

# While
i = 1
while i <= 10:
    print('[WHILE] Current iteration is', i)
    i += 1